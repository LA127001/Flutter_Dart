void main() {
  var name =
      "HA"; // var is dynamic datatype after declare but you not change the value in other data type
  String str = "Hello world"; // store text value
  int n1 = 5; // store int value
  double n2 = 3.5; // store double value
  num n3 = 223.23; // store both value (int & double)
  bool n4 = true; // store true & false
  const pi = 3.14; // constant is immutable
  dynamic myVariable =
      50; // store dynamic value change any other data type value
}
